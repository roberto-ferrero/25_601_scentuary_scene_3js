import Pilar3D from "./Pilar3D"

class PilarsGroup{
    constructor (obj){
        // console.log("(PilarsGroup.CONSTRUCTORA): ", obj)
        this.app = obj.app
        this.project = obj.project
        this.stage = obj.stage
        this.parent3D = obj.parent3D
        //-----------------------------
        this.ITEM_MANIFEST = ["pilar1", "pilar2", "pilar3", "pilar4", "pilar5", "pilar6", "pilar7", "pilar8", "pilar9"]
        this.ITEMS_REF = []
        this.ITEM_MANIFEST.map((itemId)=>{
            const item = new Pilar3D({
                app:this.app,
                project:this.project,
                stage:this.stage,
                parent3D:this.parent3D,
                itemId:itemId
            })
            this.ITEMS_REF.push(item)
        })
    }
    //----------------------------------------------
    // PUBLIC:
    
    //----------------------------------------------
    // EVENTS:

    //----------------------------------------------
    // PRIVATE:

    //----------------------------------------------
    // AUX:

  
}
export default PilarsGroup